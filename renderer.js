document.getElementById('btn-mods-launch').addEventListener('click', async () => {
  const profile = _modProfiles.find(p => p.id === _currentProfileId);
  if (!profile) return;

  const launchBtn = document.getElementById('btn-mods-launch');
  const progressWrap = document.getElementById('mods-progress-wrap');
  const progressFill = document.getElementById('mods-progress-fill');
  const progressLabel = document.getElementById('mods-progress-label');

  const setProgress = (text, pct) => {
    progressLabel.textContent = text;
    if (pct !== undefined) progressFill.style.width = pct + '%';
  };

  launchBtn.disabled = true;
  progressWrap.style.display = 'flex';
  progressFill.style.width = '0%';

  try {
    const settings = await getSettings();
    // profileDir — папка инстанса профиля (моды, конфиги, сохранения)
    const profileDir = await window.launcher.modProfileGamedir(profile.id);
    // sharedDir — общая .minecraft папка (версии, библиотеки, ассеты, Java)
    const sharedDir = settings.gameDir || await window.launcher.getDefaultGameDir();

    // Install loader if needed (non-vanilla)
    // Loader устанавливается в sharedDir, чтобы MCLC нашёл libraries там же
    let customVersion = null;
    if (profile.loader !== 'vanilla') {
      const loaderCheck = await window.launcher.checkLoader(profile.loader, profile.mcVersion, sharedDir);
      customVersion = loaderCheck.versionId;

      if (!loaderCheck.installed) {
        if (profile.loader === 'fabric') {
          setProgress('Установка Fabric...', 10);
          window.launcher.off('mod-status');
          window.launcher.on('mod-status', ({ stage }) => {
            if (stage === 'fetch-loader') setProgress('Получение Fabric loader...', 20);
            if (stage === 'fetch-profile') setProgress('Загрузка профиля Fabric...', 35);
          });
          const r = await window.launcher.installFabric(profile.mcVersion, sharedDir);
          window.launcher.off('mod-status');
          if (!r.success) throw new Error('Ошибка Fabric: ' + r.error);
          customVersion = r.versionId;
        } else if (profile.loader === 'forge') {
          setProgress('Загрузка Forge...', 10);
          window.launcher.on('mod-progress', ({ received, total }) => {
            setProgress(`Forge: ${(received/1024/1024).toFixed(1)} МБ`, total ? 10 + Math.round((received/total)*50) : 30);
          });
          window.launcher.on('mod-status', ({ stage, line }) => {
            if (stage === 'installing-forge') setProgress('Установка Forge...', 65);
            if (stage === 'forge-log' && line) appendConsole('info', '[Forge] ' + line);
          });
          const r = await window.launcher.installForge(profile.mcVersion, sharedDir);
          window.launcher.off('mod-progress');
          window.launcher.off('mod-status');
          if (!r.success) throw new Error('Ошибка Forge: ' + r.error);
          customVersion = r.versionId;
        } else if (profile.loader === 'neoforge') {
          setProgress('Загрузка NeoForge...', 10);
          window.launcher.on('mod-progress', ({ received, total }) => {
            setProgress(`NeoForge: ${(received/1024/1024).toFixed(1)} МБ`, total ? 10 + Math.round((received/total)*50) : 30);
          });
          window.launcher.on('mod-status', ({ stage, line }) => {
            if (stage === 'installing-neoforge') setProgress('Установка NeoForge...', 65);
            if (stage === 'neoforge-log' && line) appendConsole('info', '[NeoForge] ' + line);
          });
          const r = await window.launcher.installNeoforge(profile.mcVersion, sharedDir);
          window.launcher.off('mod-progress');
          window.launcher.off('mod-status');
          if (!r.success) throw new Error('Ошибка NeoForge: ' + r.error);
          customVersion = r.versionId;
        }
      }
    }

    // Ensure vanilla jar (в sharedDir — общий для всех профилей одной версии)
    setProgress(`Проверка Minecraft ${profile.mcVersion}...`, 55);
    const vanillaOk = await window.launcher.checkVersion(sharedDir, profile.mcVersion);
    if (!vanillaOk) {
      setProgress(`Скачиваем Minecraft ${profile.mcVersion}...`, 60);
      const vr = await window.launcher.ensureVanilla(profile.mcVersion, sharedDir);
      if (!vr.success) throw new Error('Ошибка загрузки ванилы: ' + vr.error);
    }

    // Java — тоже в sharedDir, чтобы не скачивать для каждого профиля
    setProgress('Проверка Java...', 75);
    const javaPath = await ensureJava(profile.mcVersion, sharedDir, settings);

    setProgress('Запуск...', 90);
    window.launcher.off('launch-progress'); window.launcher.off('launch-log'); window.launcher.off('launch-close');
    window.launcher.on('launch-progress', e => {
      const pct = e.total ? Math.round((e.task / e.total) * 100) : 0;
      setProgress(`${e.type || 'Загрузка'} — ${pct}%`, pct);
    });
    window.launcher.on('launch-log', d => appendConsole(d.type, d.msg));
    const modsStopBtn = document.getElementById('btn-mods-stop');
    window.launcher.on('launch-close', code => {
      appendConsole('info', `Игра завершена (код ${code})`);
      launchBtn.disabled = false;
      progressWrap.style.display = 'none';
      progressFill.style.width = '0%';
      if (modsStopBtn) modsStopBtn.style.display = 'none';
      if (code !== 0) showCrashModal(_lastLaunchGameDir, _launchTimestamp);
    });

    _launchTimestamp = Date.now();
    _lastLaunchGameDir = profileDir;
    const launchOpts = {
      version: profile.mcVersion,
      customVersion: customVersion || undefined,
      versionType: 'release',
      gameDir: profileDir,   // папка мод-профиля (моды, конфиги, сохранения)
      maxRam: settings.maxRam || 4,
      minRam: settings.minRam || 2,
      javaPath: javaPath || 'java',
      jvmArgs: settings.jvmArgs || '',
      winWidth: settings.winWidth || 854,
      winHeight: settings.winHeight || 480,
      fullscreen: settings.fullscreen || false,
      hideLauncher: settings.hideLauncher !== false,
      closeLauncher: settings.closeLauncher || false,
      assetMirror: settings.assetMirror || '',
      sharedDir: sharedDir   // shared .minecraft (для root/libraries/assets в MCLC)
    };
    const result = await window.launcher.launch(launchOpts);
    if (!result.success) throw new Error(result.error);

    setProgress('Игра запущена', 100);
    if (modsStopBtn) modsStopBtn.style.display = 'flex';
  } catch (e) {
    appendConsole('error', 'Ошибка запуска профиля: ' + e.message);
    launchBtn.disabled = false;
    progressWrap.style.display = 'none';
    if (document.getElementById('btn-mods-stop')) document.getElementById('btn-mods-stop').style.display = 'none';
  }
});

